<script lang="ts">
	import { onMount } from 'svelte';
	import { get } from 'svelte/store';
	import { goto } from '$app/navigation';
	import { joinEnrollNode, createJoinLink, getServerEnrollCommand } from '$lib/tauri';
	import { pendingInvite } from '$lib/stores';
	import { encodeQR } from '$lib/qr';
	import { runWithStepUp, isStepUpRequired } from '$lib/stepup';

	// Device vs server: two different node kinds (Part B §B.1.1 — a Node without an
	// owner is a service/server). Device = scan QR with the Ankayma app. Server = no
	// app to run, so a copy-paste CLI command instead.
	let mode = $state<'device' | 'server'>('device');
	let serverCmd = $state<string | null>(null);
	let serverCmdLoading = $state(false);
	let serverCmdError = $state<string | null>(null);
	let serverCmdRevealed = $state(false);
	let serverCmdCopied = $state(false);

	async function loadServerCmd() {
		if (serverCmd || serverCmdLoading) return;
		serverCmdLoading = true;
		serverCmdError = null;
		try {
			serverCmd = await getServerEnrollCommand();
		} catch (e) {
			serverCmdError = e instanceof Error ? e.message : 'Failed to build the enroll command';
		} finally {
			serverCmdLoading = false;
		}
	}

	function selectMode(m: 'device' | 'server') {
		mode = m;
		if (m === 'server') loadServerCmd();
	}

	async function copyServerCmd() {
		if (!serverCmd) return;
		try {
			await navigator.clipboard.writeText(serverCmd);
			serverCmdCopied = true;
			setTimeout(() => (serverCmdCopied = false), 2000);
		} catch {
			// clipboard not available
		}
	}

	// Real join link generated by the control plane (single-use enrollment token).
	let joinLink = $state<string | null>(null);
	let loading  = $state(true);
	let copied   = $state(false);

	// Configurable node-invite TTL (server clamps to [1m, 7d]). [A] invite-flow §TTL.
	const NODE_TTL_OPTIONS = [
		{ label: '1 hour',  secs: 3600 },
		{ label: '6 hours', secs: 21600 },
		{ label: '24 hours', secs: 86400 },
		{ label: '7 days',  secs: 604800 },
	];
	let nodeTtl = $state(86400);
	let ttlLabel = $derived(NODE_TTL_OPTIONS.find((o) => o.secs === nodeTtl)?.label ?? '24 hours');

	// QR is rendered dependency-free onto a canvas (so it also exports to PNG).
	let qrCanvas = $state<HTMLCanvasElement | null>(null);
	$effect(() => {
		if (qrCanvas && joinLink) drawQR(qrCanvas, joinLink);
	});

	function drawQR(canvas: HTMLCanvasElement, text: string) {
		const { size, modules } = encodeQR(text);
		const quiet = 4;
		const scale = 5;
		const dim = (size + quiet * 2) * scale;
		canvas.width = dim;
		canvas.height = dim;
		const ctx = canvas.getContext('2d');
		if (!ctx) return;
		ctx.fillStyle = '#ffffff';
		ctx.fillRect(0, 0, dim, dim);
		ctx.fillStyle = '#000000';
		for (let r = 0; r < size; r++) {
			for (let c = 0; c < size; c++) {
				if (modules[r][c]) ctx.fillRect((c + quiet) * scale, (r + quiet) * scale, scale, scale);
			}
		}
	}

	function exportQR() {
		if (!qrCanvas) return;
		const a = document.createElement('a');
		a.download = 'ankayma-invite.png';
		a.href = qrCanvas.toDataURL('image/png');
		a.click();
	}

	// Recipient side (Cases C/D): arriving here via a node-invite deep link — i.e. the
	// recipient SCANNED the QR or opened `ankayma://join?token=…` — AUTO-enrolls this
	// device. No paste box, no confirm: scan = enrolled.
	// Founder decision 2026-06-27. (The spec only mandates "token pre-filled"; auto vs
	// confirm is an impl choice — see part-d-invite-flow.md §Cases C/D.)
	let enrolling = $state(false);
	let joinError = $state<string | null>(null);
	let joinDone  = $state(false);

	onMount(async () => {
		const invite = get(pendingInvite);
		if (invite?.type === 'join-node') {
			// Received an invite → enroll this device automatically, then go home.
			pendingInvite.set(null);
			enrolling = true;
			try {
				const m = invite.token.match(/token=([^&\s]+)/);
				const tok = m ? m[1] : invite.token.trim();
				await joinEnrollNode(tok, ''); // unnamed — rename later in Devices
				joinDone = true;
				setTimeout(() => goto('/services'), 800);
			} catch (e: unknown) {
				joinError = e instanceof Error ? e.message : 'Enrollment failed';
			}
			return;
		}
		// Otherwise this is the "invite another device" page → mint an invite QR.
		loadJoinLink();
	});

	// Multi-user tenants gate invite minting behind a step-up. We DON'T pop the OTP
	// modal on page load (that would email a code on mere navigation); instead we
	// detect the gate and show a "Generate invite link" button the admin opts into.
	let needsStepUp = $state(false);

	async function loadJoinLink() {
		loading = true;
		try {
			// control-plane returns a single-use enrollment token URL
			// ankayma://join?token=<token>&tenant=<id>, valid for the chosen TTL.
			joinLink = await createJoinLink(nodeTtl);
			needsStepUp = false;
		} catch (e) {
			if (isStepUpRequired(e)) {
				// Gated — surface an opt-in button rather than auto-minting.
				joinLink = null;
				needsStepUp = true;
			} else {
				// Device-link enrollment isn't wired to the control plane yet — don't
				// fabricate a token that looks real. Fall through to the honest
				// "coming soon" state below.
				joinLink = null;
			}
		} finally {
			loading = false;
		}
	}

	// Admin opts into the gated mint: run the OTP step-up, then mint with the proof.
	async function generateWithStepUp() {
		try {
			joinLink = await runWithStepUp('enroll_node', (proof) => createJoinLink(nodeTtl, proof));
			needsStepUp = false;
		} catch {
			// cancelled or failed — keep the opt-in button.
		}
	}

	// Re-mint with a fresh TTL when the admin picks a different expiry.
	function onTtlChange() {
		joinLink = null;
		loadJoinLink();
	}

	async function copyLink() {
		if (!joinLink) return;
		try {
			await navigator.clipboard.writeText(joinLink);
			copied = true;
			setTimeout(() => (copied = false), 2000);
		} catch {
			// clipboard not available (mobile webview)
		}
	}
</script>

