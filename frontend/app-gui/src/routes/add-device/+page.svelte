				</code>
				<button class="copy-btn" onclick={() => (serverCmdRevealed = !serverCmdRevealed)} aria-label={serverCmdRevealed ? 'Hide token' : 'Show token'}>
					{#if serverCmdRevealed}
						<svg width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2"><path d="M17.94 17.94A10.94 10.94 0 0112 20c-7 0-11-8-11-8a18.45 18.45 0 015.06-5.94M9.9 4.24A9.12 9.12 0 0112 4c7 0 11 8 11 8a18.5 18.5 0 01-2.16 3.19M1 1l22 22"/></svg>
					{:else}
						<svg width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2"><path d="M1 12s4-8 11-8 11 8 11 8-4 8-11 8-11-8-11-8z"/><circle cx="12" cy="12" r="3"/></svg>
					{/if}
				</button>
				<button class="copy-btn" onclick={copyServerCmd} aria-label="Copy command">
					{#if serverCmdCopied}
						<svg width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="var(--c-success)" stroke-width="2.5">
							<path d="M20 6L9 17l-5-5"/>
						</svg>
					{:else}
						<svg width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2">
							<rect x="9" y="9" width="13" height="13" rx="2"/><path d="M5 15H4a2 2 0 01-2-2V4a2 2 0 012-2h9a2 2 0 012 2v1"/>
						</svg>
					{/if}
				</button>
			</div>
			<p class="qr-note">
				SSH into the server and run this command — it enrolls and brings the tunnel up
				directly, no QR or app needed.
			</p>
		{/if}
		{/if}
	{/if}
	</div>
</main>

